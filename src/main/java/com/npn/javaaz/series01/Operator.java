package com.npn.javaaz.series01;

public class Operator {
    /*
        + , - , * , / , %
    */

    public static void main(String[] args) {

        int a = 18;
        int b = 2;

        int result1 = a + b;
        System.out.println("a + b : a plus b = " + result1);

        int result2 = a - b;
        System.out.println("a - b : a minus b = " + result2);

        int result3 = a * b;
        System.out.println("a * b : a times b = " + result3);

        int result4 = a / b;
        System.out.println("a / b : a divided by b = " + result4);

        int result5 = b % a;
        System.out.println("a % b : a modulo b = " + result5);



    }
}
